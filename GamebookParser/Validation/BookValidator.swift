import Foundation

// ============================================================================
// MARK: - ВАЛИДАТОР КНИГИ: ПРОВЕРКА ЦЕЛОСТНОСТИ ДАННЫХ
// ============================================================================

/// Валидатор структуры книги-игры, обнаруживающий логические ошибки и предупреждения.
/// - Проверяет достижимость страниц, корректность ссылок, тупики и циклы
/// - Использует алгоритмы обхода графа (DFS — Depth-First Search)
/// - Возвращает массив проблем с указанием серьёзности (.error / .warning)
final class BookValidator {
    
    // MARK: - 🎯 ОСНОВНОЙ МЕТОД ВАЛИДАЦИИ
    /// Выполняет полную проверку книги и возвращает список найденных проблем.
    /// - Parameter book: Модель книги для проверки
    /// - Returns: Массив ValidationIssue, пустой если ошибок нет
    /// - Сложность: O(V + E), где V = страницы, E = переходы между ними
    func validate(book: Book) -> [ValidationIssue] {
        // Локальный массив для накопления найденных проблем
        var issues: [ValidationIssue] = []
        
        // ─────────────────────────────────────────────────────
        // ПОДГОТОВКА: МНОЖЕСТВО ВСЕХ ID СТРАНИЦ
        // Set обеспечивает быструю проверку принадлежности за O(1)
        let allPages = Set(book.pages.keys)
        
        // ====================================================================
        // ПРОВЕРКА 1: ДОСТИЖИМОСТЬ СТРАНИЦ (REACHABILITY)
        // ====================================================================
        // Цель: убедиться, что все страницы можно посетить, начиная с первой
        // Алгоритм: DFS (поиск в глубину) от стартовой страницы
        
        /// Множество уже посещённых страниц в процессе обхода
        var reachable: Set<Int> = []
        
        /// Вложенная рекурсивная функция DFS для обхода графа переходов
        /// - Parameter page: ID текущей страницы для обработки
        func dfs(_ page: Int) {
            // Базовый случай рекурсии: страница уже обработана — выходим
            // Это предотвращает бесконечную рекурсию при циклах
            guard !reachable.contains(page) else {
                return
            }
            
            // Помечаем страницу как посещённую
            reachable.insert(page)
            
            // Безопасное получение объекта страницы из книги
            guard let current = book.pages[page] else {
                return // Страница не найдена в словаре (должно быть обработано отдельно)
            }
            
            // Рекурсивный обход всех целевых страниц переходов типа "choice"
            for next in current.actions.choice.keys {
                dfs(next) // Рекурсивный вызов для следующей страницы
            }
        }
        
        // Запускаем DFS от первой страницы (по отсортированному ID)
        // sorted() гарантирует детерминированный порядок обхода
        if let start = allPages.sorted().first {
            dfs(start)
        }
        
        // ─────────────────────────────────────────────────────
        // АНАЛИЗ РЕЗУЛЬТАТОВ ОБХОДА: ПОИСК НЕДОСТИЖИМЫХ СТРАНИЦ
        for page in allPages.sorted() {
            if !reachable.contains(page) {
                // Страница существует, но до неё нельзя добраться из старта
                issues.append(
                    ValidationIssue(
                        severity: .warning, // Не критично, но требует внимания автора
                        page: page,
                        message: "Страница недостижима" // Сообщение для отображения в UI
                    )
                )
            }
        }
        
        // ====================================================================
        // ПРОВЕРКА 2: НЕВАЛИДНЫЕ ССЫЛКИ (INVALID LINKS)
        // ====================================================================
        // Цель: убедиться, что все переходы указывают на существующие страницы
        // Это критическая ошибка: битая ссылка = крах игры для игрока
        
        for (_, page) in book.pages {
            for target in page.actions.choice.keys {
                // Проверяем наличие целевой страницы в множестве всех страниц
                if !allPages.contains(target) {
                    issues.append(
                        ValidationIssue(
                            severity: .error, // Критическая ошибка, блокирующая экспорт
                            page: page.id,    // ID страницы-источника проблемы
                            message: "Переход на несуществующую страницу \(target)"
                        )
                    )
                }
            }
        }
        
        // ====================================================================
        // ПРОВЕРКА 3: ТУПИКИ (DEAD ENDS)
        // ====================================================================
        // Цель: найти страницы без вариантов выбора, которые не помечены как финальные
        // Эвристика: если текст содержит слово "конец" — считаем страницу финальной
        
        for (_, page) in book.pages {
            // Есть ли у страницы варианты перехода (выборы)?
            let hasChoices = !page.actions.choice.isEmpty
            
            // Является ли страница финальной по контенту?
            // - Объединяем все абзацы в одну строку
            // - Приводим к нижнему регистру для регистронезависимого поиска
            // - Ищем ключевое слово "конец" (можно расширить: "финиш", "the end")
            let isEnding = page.text
                .joined()
                .lowercased()
                .contains("конец")
            
            // Если нет выборов И страница не помечена как финальная — подозрительный тупик
            if !hasChoices && !isEnding {
                issues.append(
                    ValidationIssue(
                        severity: .warning, // Предупреждение: возможно, автор забыл добавить выбор
                        page: page.id,
                        message: "Возможный dead-end" // Страница без выхода
                    )
                )
            }
        }
        
        // ====================================================================
        // ПРОВЕРКА 4: ЦИКЛЫ В ГРАФЕ (CYCLES)
        // ====================================================================
        // Цель: обнаружить бесконечные циклы переходов (страница → ... → сама себя)
        // Циклы не всегда ошибка, но часто указывают на логическую проблему
        
        // Делегируем сложную логику отдельному методу и добавляем результаты к общему списку
        issues += detectCycles(book: book)
        
        // Возвращаем полный отчёт о валидации
        return issues
    }
    
    // MARK: - 🔁 ОБНАРУЖЕНИЕ ЦИКЛОВ (CYCLE DETECTION)
    /// Приватный метод для поиска циклов в графе переходов с помощью DFS с отслеживанием стека.
    /// - Алгоритм: классический DFS с тремя состояниями вершины:
    ///   1. Не посещена (не в visited)
    ///   2. В процессе обработки (в stack) ← если попали сюда повторно = цикл!
    ///   3. Полностью обработана (в visited, но не в stack)
    /// - Parameter book: Модель книги для анализа
    /// - Returns: Массив предупреждений о найденных циклах
    private func detectCycles(book: Book) -> [ValidationIssue] {
        var issues: [ValidationIssue] = []
        
        /// Множество полностью обработанных страниц (чтобы не проверять дважды)
        var visited: Set<Int> = []
        
        /// Множество страниц в текущем пути обхода (стек рекурсии)
        /// Используется для детектирования обратных рёбер = циклов
        var stack: Set<Int> = []
        
        /// Вложенная рекурсивная функция DFS с отслеживанием пути
        /// - Parameter page: ID текущей страницы
        func dfs(_ page: Int) {
            // ─────────────────────────────────────────────────────
            // ДЕТЕКТИРОВАНИЕ ЦИКЛА
            // Если страница уже в текущем стеке пути — мы вернулись в неё же = цикл!
            if stack.contains(page) {
                issues.append(
                    ValidationIssue(
                        severity: .warning, // Цикл может быть преднамеренным (головоломка)
                        page: page,
                        message: "Обнаружен цикл"
                    )
                )
                // Не возвращаемся сразу: нужно продолжить обход для поиска других циклов
                return
            }
            
            // Если страница уже полностью обработана ранее — пропускаем
            // Это оптимизация: не нужно заново обходить уже проверенные подграфы
            if visited.contains(page) {
                return
            }
            
            // Помечаем страницу как "в процессе обработки" (добавляем в путь)
            visited.insert(page)
            stack.insert(page)
            
            // Безопасное получение объекта страницы
            guard let current = book.pages[page] else {
                return
            }
            
            // Рекурсивный обход всех исходящих переходов
            for next in current.actions.choice.keys {
                dfs(next)
            }
            
            // ─────────────────────────────────────────────────────
            // BACKTRACKING: удаляем страницу из текущего пути
            // После обработки всех потомков страница больше не в активном пути
            stack.remove(page)
        }
        
        // Запускаем поиск циклов от первой страницы
        // Примечание: если граф несвязный, могут потребоваться дополнительные запуски
        // от каждой непосещённой вершины (для полноты проверки)
        if let start = book.pages.keys.sorted().first {
            dfs(start)
        }
        
        return issues
    }
}
