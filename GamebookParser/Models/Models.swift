/// Операции со спутниками: присоединение и отсоединение от группы игрока.
/// - Аналогичен ItemOperations, но работает с CharacterStats вместо количества
/// - Позволяет передавать характеристики спутника при его получении
///
/// ## Отличия от ItemOperations:
/// • Значение словаря — CharacterStats, а не Int
///   → спутник — это персонаж с навыком и здоровьем, а не просто "количество"
/// • Обычно dec используется реже: спутники чаще присоединяются, чем покидают
/// • Имя спутника должно быть уникальным ключом (нельзя двух "Гэндальфов")
///
/// ## Пример: получение спутника
/// ```swift
/// // Страница: "Гэндальф присоединяется к вам (Навык: 8, Здоровье: 12)"
/// let ops = CompanionOperations(
///     add: ["Гэндальф": CharacterStats(skill: 8, vitality: 12)],
///     dec: [:]
/// )
///
/// // Добавление в группу игрока:
/// for (name, stats) in ops.add {
///     playerParty[name] = stats
/// }
/// ```
struct CompanionOperations: Codable {
    /// Спутники, которые ПРИСОЕДИНЯЮТСЯ к игроку на этой странице.
    /// - Ключ: уникальное имя спутника ("Гэндальф", "Леголас", "Верный Пёс")
    /// - Значение: начальные характеристики спутника при получении
    /// - Важно: если спутник уже есть в группе — решить: проигнорировать / обновить / ошибка
    var add: [String: CharacterStats]
    
    /// Спутники, которые ПОКИДАЮТ группу игрока на этой странице.
    /// - Ключ: имя спутника для удаления (должен точно совпадать)
    /// - Значение: характеристики (могут использоваться для лога: "Гэндальф ушёл с 5 здоровья")
    /// - Пустой словарь: никто не уходит со страницы
    var dec: [String: CharacterStats]
    
    /// Пустой экземпляр для использования по умолчанию (паттерн Null Object).
    /// - Упрощает инициализацию страниц без операций со спутниками
    /// - Избегает опционалов: var companions: CompanionOperations = .empty
    static let empty = CompanionOperations(add: [:], dec: [:])
}

