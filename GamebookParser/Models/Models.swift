/// Операции с предметами инвентаря: добавление и удаление.
/// - Используется словарная структура для быстрого доступа по названию предмета
/// - Значение = количество: позволяет учитывать стаки предметов ("3 зелья")
///
/// ## Пример использования:
/// ```swift
/// // Страница: "Вы нашли меч и 2 зелья"
/// let ops = ItemOperations(
///     add: ["меч": 1, "зелье": 2],
///     dec: [:]
/// )
///
/// // Применение к инвентарю игрока:
/// for (item, count) in ops.add {
///     playerInventory[item] = (playerInventory[item] ?? 0) + count
/// }
/// ```
///
/// ## Почему два отдельных словаря (add/dec), а не один с знаками?
/// ✅ **Явность**: сразу видно, что добавляется, а что удаляется — меньше ошибок
/// ✅ **Валидация**: легче проверить, что dec не удаляет больше, чем есть у игрока
/// ✅ **UI**: можно визуально разделить секции "Получить" / "Потерять" в редакторе
/// ✅ **Логика**: некоторые предметы могут одновременно добавляться и удаляться (обмен)
struct ItemOperations: Codable {
    /// Предметы, которые игрок ПОЛУЧИТ при посещении страницы.
    /// - Ключ: название предмета ("меч", "золото", "ключ_от_темницы")
    /// - Значение: количество (1 = единичный предмет, 50 = стопка золота)
    /// - Пустой словарь: нет новых предметов на этой странице
    var add: [String: Int]
    
    /// Предметы, которые игрок ПОТЕРЯЕТ/ОТДАСТ при посещении страницы.
    /// - Ключ: название предмета (должен совпадать с ключом в инвентаре)
    /// - Значение: количество к удалению
    /// - Важно: валидатор должен проверять, что у игрока достаточно предметов для удаления
    var dec: [String: Int]
    
    /// Пустой экземпляр для использования по умолчанию.
    /// - Паттерн "Null Object": избегает проверок на nil, упрощает код
    /// - Использование: var items: ItemOperations = .empty
    /// - Пример: если страница не имеет операций с предметами — ставим .empty
    static let empty = ItemOperations(add: [:], dec: [:])
}

