/// Корневая модель книги-игры, содержащая метаданные и все страницы.
/// - Соответствует протоколу `Codable` для автоматической сериализации в JSON
/// - Используется как основной тип данных во ViewModel, парсере и валидаторе
///
/// ## Пример JSON-представления:
/// ```json
/// {
///   "name": "Приключения в Средиземье",
///   "author": "Дж. Р. Р. Толкин",
///   "pages": {
///     "0": { "id": 0, "text": ["Вы вышли из дома..."], ... },
///     "1": { "id": 1, "text": ["Перед вами лес..."], ... }
///   }
/// }
/// ```
///
/// ## Почему словарь [Int: Page], а не массив [Page]?
/// ✅ **Быстрый доступ**: book.pages[42] — O(1) вместо перебора массива
/// ✅ **Гарантия уникальности**: ключ словаря не позволяет дублировать ID страниц
/// ✅ **Гибкость нумерации**: страницы могут быть 1, 5, 10, 42 — без пропусков в индексах
/// ✅ **Естественное отображение**: номер страницы = ключ, интуитивно для автора
///
/// ## Особенности Codable:
/// • Автоматически кодирует/декодирует все свойства с теми же именами
/// • Для кастомных имён ключей в JSON используется enum CodingKeys
/// • При декодировании отсутствующие поля получают значения по умолчанию (если есть)
struct Book: Codable {
    /// Отображаемое название книги (для заголовка, экспорта, автосохранения)
    var name: String
    
    /// Автор книги (метаданные, могут использоваться для атрибуции или статистики)
    var author: String
    
    /// Словарь всех страниц книги: ключ = номер страницы, значение = объект Page
    /// - Страницы не гарантируют порядок при итерации — используйте .sorted() если нужен порядок
    /// - Пустая книга: pages = [:] (валидное состояние, означает "нет страниц")
    var pages: [Int: Page]
}

