/// Базовые характеристики персонажа (врага, спутника, игрока).
/// - Используется для расчёта исхода боёв и проверок навыков
/// - Простая структура из двух Int для лёгкой сериализации и редактирования
///
/// ## Система характеристик (классическая для книг-игр):
/// • **skill (навык/мастерство)**: 0–99 — вероятность успеха в бою/проверке
///   - 0 = неумеха, 10 = новичок, 50 = опытный, 99 = легендарный
/// • **vitality (жизнеспособность/здоровье)**: 0–999 — запас "очков жизни"
///   - Уменьшается при получении урона, 0 = смерть/вывод из строя
///
/// ## Пример расчёта боя (упрощённо):
/// ```swift
/// func resolveCombat(playerSkill: Int, enemy: CharacterStats) -> Bool {
///     let playerRoll = Int.random(in: 1...6) + playerSkill
///     let enemyRoll = Int.random(in: 1...6) + enemy.skill
///     return playerRoll > enemyRoll // true = игрок выиграл раунд
/// }
/// ```
///
/// ## Почему только два параметра?
/// ✅ **Простота**: легко понять и редактировать новичку
/// ✅ **Достаточность**: покрывает базовые механики большинства книг-игр
/// ✅ **Расширяемость**: можно добавить luck, magic, armor через наследование или композицию
struct CharacterStats: Codable {
    /// Навык/мастерство персонажа — влияет на вероятность успеха действий.
    /// - Диапазон: 0...99 (рекомендуется валидировать при вводе)
    /// - Использование: броски кубиков, сравнение с противником, проверки навыков
    var skill: Int
    
    /// Жизнеспособность/здоровье — запас прочности персонажа.
    /// - Диапазон: 0...999 (больше, чем skill, т.к. урон может быть частым)
    /// - Использование: уменьшение при получении урона, проверка на "смерть" при 0
    var vitality: Int
}

