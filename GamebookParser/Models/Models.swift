/// Контейнер всех доступных действий на странице: безусловные выборы и условные проверки.
/// - Объединяет два типа навигации в одном свойстве для удобства доступа
/// - Используется в редакторе, валидаторе и игровом движке
///
/// ## Разделение на check и choice — зачем?
/// • **choice**: всегда видимые и доступные переходы ("Пойти налево", "Атаковать")
/// • **check**: скрытые переходы, открывающиеся при выполнении условий ("Если есть ключ → открыть дверь")
/// • Это разделение упрощает:
///   - Валидацию: проверять, что условные переходы ведут на существующие страницы
///   - Геймплей: показывать игроку только доступные сейчас варианты
///   - Редактор: визуально отделять обычные и условные переходы
///
/// ## Пример полной структуры:
/// ```swift
/// PageActions(
///     check: CheckActions(values: [
///         "ключ_от_темницы": ["42": "Отпереть дверь"]
///     ]),
///     choice: [
///         15: "Пойти налево в коридор",
///         23: "Пойти направо к лестнице",
///         7: "Остаться на месте и прислушаться"
///     ]
/// )
/// ```
struct PageActions: Codable {
    /// Условные действия, требующие проверки перед отображением/активацией.
    /// - См. документацию CheckActions для деталей структуры
    /// - Обычно заполняется парсером при обнаружении конструкций типа "Если есть Х, иди на Y"
    var check: CheckActions
    
    /// Безусловные переходы, доступные игроку всегда.
    /// - Ключ: номер целевой страницы (Int для быстрого доступа и валидации)
    /// - Значение: текст описания выбора, отображаемый в UI ("Открыть сундук")
    /// - Пустой словарь: на странице нет выборов (возможно, это финал или тупик)
    /// - Важно: валидатор проверяет, что все target-страницы существуют в книге
    var choice: [Int: String]
    
    /// Пустой экземпляр для страниц без действий (финалы, описательные страницы).
    /// - Упрощает инициализацию: actions = .empty вместо ручного создания
    /// - Избегает опционалов: не нужно проверять if actions != nil
    static let empty = PageActions(check: .empty, choice: [:])
}

