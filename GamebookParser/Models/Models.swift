/// Условные действия, требующие проверки перед выполнением перехода.
/// - Реализует механику "если есть предмет / если навык >= X, то доступен выбор"
/// - Используется для нелинейного геймплея и скрытых путей
///
/// ## Структура данных:
/// ```
/// values: [
///   "ключ_от_темницы": [  // Требуемый предмет/условие
///     "15": "Отпереть дверь и войти",  // Если условие выполнено → переход на 15 с этим текстом
///     "23": "Поискать другой вход"     // Альтернатива? (опционально)
///   ],
///   "skill >= 10": [
///     "42": "Перепрыгнуть пропасть"
///   ]
/// ]
/// ```
///
/// ## Почему [String: [String: String]]?
/// • Внешний ключ: условие ("ключ", "skill >= 10", "has_met_wizard")
/// • Внутренний словарь: [номер_страницы: текст_выбора]
/// • Это позволяет одному условию открывать несколько альтернативных переходов
///
/// ## Пример использования в логике:
/// ```swift
/// func getAvailableChoices(page: Page, playerInventory: [String: Int]) -> [Int: String] {
///     var available = page.actions.choice // Безусловные переходы
///
///     // Добавляем условные, если требования выполнены
///     for (condition, targets) in page.actions.check.values {
///         if isConditionMet(condition, inventory: playerInventory) {
///             available.merge(targets) { current, _ in current } // Приоритет безусловным
///         }
///     }
///     return available
/// }
/// ```
///
/// ## Идеи для расширения:
/// • Добавить тип условия: enum ConditionType { case item(String), case skill(Int), case flag(String) }
/// • Поддержать логические операторы: "ключ И факел", "навык >= 10 ИЛИ удача >= 15"
/// • Визуализация в редакторе: показывать замочек 🔒 рядом с заблокированными выборами
struct CheckActions: Codable {
    /// Словарь условий и соответствующих им переходов.
    /// - Формат: [условие: [номер_страницы: текст_описания]]
    /// - Пустой словарь: на странице нет условных действий
    /// - Пример: values["ключ"] = ["15": "Открыть дверь ключом"]
    var values: [String: [String: String]]
    
    /// Пустой экземпляр для страниц без условных действий.
    /// - Паттерн Null Object: упрощает код, избегает опционалов
    /// - Использование: var check: CheckActions = .empty
    static let empty = CheckActions(values: [:])
}

