/// Модель одной страницы книги-игры, содержащая контент и игровую логику.
/// - Соответствует `Codable` для сериализации в JSON
/// - Соответствует `Identifiable` для интеграции со SwiftUI List/ForEach
///
/// ## Жизненный цикл страницы:
/// 1. **Парсинг**: BookParser создаёт Page из сырого текста
/// 2. **Редактирование**: пользователь меняет text/actions/items в PageEditorView
/// 3. **Валидация**: BookValidator проверяет целостность ссылок и логики
/// 4. **Экспорт**: страница кодируется в JSON вместе с остальной книгой
///
/// ## Почему text — это [String], а не String?
/// ✅ **Абзацы**: каждый элемент массива = отдельный абзац/блок текста
/// ✅ **Форматирование**: легко добавлять отступы, стилизацию между абзацами
/// ✅ **Редактирование**: TextEditor в SwiftUI работает с одной строкой — удобно мапить
/// ✅ **Нормализация**: TextCleaner.normalize() возвращает [String] — естественное соответствие
///
/// ## Пример использования в UI:
/// ```swift
/// // Отображение текста страницы с отступами между абзацами
/// VStack(alignment: .leading, spacing: 12) {
///     ForEach(page.text, id: \.self) { paragraph in
///         Text(paragraph)
///     }
/// }
/// ```
struct Page: Codable, Identifiable {
    /// Уникальный идентификатор страницы — номер, под которым она известна в книге.
    /// - Соответствует протоколу Identifiable.id для SwiftUI
    /// - Используется как ключ в Book.pages и для навигации между страницами
    /// - Пример: страница "иди на 42" ссылается на page.id == 42
    var id: Int
    
    /// Текст страницы, разбитый на абзацы.
    /// - Каждый элемент массива — логически завершённый блок текста
    /// - Пустой массив: страница без текста (техническая, только переходы)
    /// - Пример: ["Вы вошли в пещеру.", "В темноте мерцают глаза."]
    var text: [String]
    
    /// Операции с предметами, выполняемые при посещении страницы.
    /// - add: предметы, которые игрок получит (меч, зелье, ключ)
    /// - dec: предметы, которые игрок потеряет/отдаст
    /// - Пример: items.add["gold"] = 50 → игрок получает 50 золота
    var items: ItemOperations
    
    /// Операции со спутниками (союзниками).
    /// - add: спутники, которые присоединяются к игроку на этой странице
    /// - dec: спутники, которые покидают группу (смерть, уход, предательство)
    /// - Пример: companions.add["Гэндальф"] = CharacterStats(skill: 8, vitality: 12)
    var companions: CompanionOperations
    
    /// Враги, встречающиеся на странице.
    /// - Ключ: имя врага ("Орк", "Тролль", "Дракон")
    /// - Значение: характеристики для боя (навык, жизнеспособность)
    /// - Пустой словарь: на странице нет врагов
    /// - Пример: enemy["Гоблин"] = CharacterStats(skill: 4, vitality: 6)
    var enemy: [String: CharacterStats]
    
    /// Действия/переходы, доступные игроку на этой странице.
    /// - check: условные действия (требуют наличия предмета/характеристики)
    /// - choice: безусловные переходы на другие страницы ("иди на 15")
    /// - Пример: actions.choice[42] = "Открыть дверь и войти"
    var actions: PageActions
}

