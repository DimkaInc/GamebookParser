/// Представление проблемы, обнаруженной валидатором книги.
/// - Соответствует `Identifiable` для отображения в SwiftUI List с выделением
/// - Использует вложенный enum Severity для типизации серьёзности проблемы
///
/// ## Жизненный цикл ValidationIssue:
/// 1. **Генерация**: BookValidator создаёт экземпляр при обнаружении проблемы
/// 2. **Накопление**: массив [ValidationIssue] собирается за один проход валидации
/// 3. **Отображение**: SwiftUI ForEach показывает список с иконками (⚠️/❌)
/// 4. **Фильтрация**: можно показать только ошибки, скрыть предупреждения
/// 5. **Навигация**: тап по проблеме → переход к соответствующей странице в редакторе
///
/// ## Почему UUID() для id, а не номер страницы?
/// ✅ **Уникальность**: одна страница может иметь несколько проблем — нужен уникальный id для каждого
/// ✅ **Стабильность**: UUID не меняется при перезагрузке, в отличие от вычисляемых хешей
/// ✅ **Совместимость с SwiftUI**: Identifiable требует стабильного, уникального id
///
/// ## Пример использования в UI:
/// ```swift
/// List(validationIssues) { issue in
///     HStack {
///         Image(systemName: issue.severity == .error ? "xmark.octagon.fill" : "exclamationmark.triangle.fill")
///         VStack(alignment: .leading) {
///             Text("Страница \(issue.page)").font(.caption)
///             Text(issue.message)
///         }
///     }
///     .onTapGesture {
///         viewModel.selectedPageID = issue.page // Переход к проблемной странице
///     }
/// }
/// ```
struct ValidationIssue: Identifiable {
    /// Уровни серьёзности проблем валидации.
    /// - Вложенный enum логически группирует типы с ошибкой, не загрязняя глобальное пространство имён
    /// - Расширяем: можно добавить .info для подсказок и улучшений
    enum Severity {
        /// Предупреждение: не блокирует работу, но требует внимания автора.
        /// - Примеры: недостижимая страница, возможный тупик, цикл в графе
        /// - Цвет в UI: оранжевый/жёлтый для привлечения внимания
        case warning
        
        /// Ошибка: критическая проблема, блокирующая экспорт или запуск игры.
        /// - Примеры: переход на несуществующую страницу, дубликат ID, битая ссылка
        /// - Цвет в UI: красный для немедленного исправления
        /// - Валидатор может возвращать false, если есть хотя бы одна .error
        case error
    }
    
    /// Уникальный идентификатор для соответствия протоколу Identifiable.
    /// - Генерируется автоматически при создании экземпляра
    /// - Не сохраняется в JSON (не Codable) — создаётся заново при загрузке
    /// - Используется SwiftUI для отслеживания состояния строк в List
    let id = UUID()
    
    /// Уровень серьёзности проблемы: предупреждение или ошибка.
    /// - Определяет визуальное отображение (иконка, цвет) и поведение (блокирует экспорт или нет)
    /// - Пример: .warning для "страница недостижима", .error для "переход на страницу 999 (не существует)"
    let severity: Severity
    
    /// Номер страницы, где обнаружена проблема.
    /// - Используется для навигации: тап по проблеме → переход к этой странице в редакторе
    /// - Может быть 0 для проблем с введением/метаданными
    /// - Валидатор гарантирует, что page существует в книге (или специально отмечает несуществующие)
    let page: Int
    
    /// Человекочитаемое описание проблемы для отображения в интерфейсе.
    /// - Должно быть кратким, но информативным: "Переход на несуществующую страницу 42"
    /// - Рекомендуется локализовать эти строки через .strings файлы для мультиязычности
    /// - Можно добавить подсказки по исправлению: "...добавьте страницу 42 или измените ссылку"
    let message: String
}
