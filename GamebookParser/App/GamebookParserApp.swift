/// Главный структурный тип приложения, соответствующий протоколу `App`.
/// - Аннотация `@main` указывает компилятору, что это entry point программы
/// - Заменяет устаревший подход с `@UIApplicationMain` / `@NSApplicationMain`
/// - Управление жизненным циклом теперь декларативное: описываем ЧТО показывать, а не КАК запускать
///
/// ## Жизненный цикл SwiftUI App (упрощённо):
/// ```
/// 1. Запуск процесса → 2. Инициализация GamebookParserApp
///                      ↓
/// 3. Вызов body → 4. Создание Scene (WindowGroup)
///                      ↓
/// 5. Инициализация ContentView → 6. Отрисовка интерфейса
///                      ↓
/// 7. Приложение готово к взаимодействию с пользователем
/// ```
///
/// ## Преимущества нового жизненного цикла:
/// ✅ **Единый код для всех платформ**: iOS, macOS, watchOS, tvOS используют один протокол App
/// ✅ **Декларативность**: описываем структуру, а не императивные шаги запуска
/// ✅ **Тестируемость**: можно инстанцировать App в юнит-тестах для проверки конфигурации
/// ✅ **Гибкость**: легко добавлять дополнительные сцены, окна, документную архитектуру
///
/// ## Важное ограничение:
/// • В теле App нельзя использовать @State, @ObservedObject — это не View!
/// • Для глобального состояния используйте:
///   - @StateObject в корневом View (ContentView)
///   - EnvironmentObject для инъекции зависимостей
///   - Статические свойства или синглтоны (осторожно!)
@main
struct GamebookParserApp: App {
    
    // MARK: - 🎨 КОНФИГУРАЦИЯ СЦЕН (SCENES)
    /// Описывает иерархию сцен приложения — окон, документов, настроек.
    /// - Возвращает тип, соответствующий протоколу `Scene`
    /// - Вызывается один раз при запуске, не пересоздаётся при перерисовках
    ///
    /// ## Что такое Scene?
    /// Scene — это "контейнер верхнего уровня" для контента приложения:
    /// • **WindowGroup**: одно или несколько окон с одинаковым корневым View
    /// • **DocumentGroup**: окна для работы с файлами (документная архитектура)
    /// • **Settings**: панель настроек приложения (macOS)
    /// • **MenuBarExtra**: элемент в строке меню (macOS)
    ///
    /// ## Почему именно WindowGroup?
    /// ✅ Простота: одно окно с корневым ContentView — идеально для утилиты-парсера
    /// ✅ Гибкость: на macOS пользователь может открыть несколько окон (Cmd+N)
    /// ✅ Адаптивность: на iOS автоматически превращается в единственное окно приложения
    ///
    /// ## Альтернативы и когда их использовать:
    /// ```swift
    /// // Документная архитектура (для работы с множеством файлов):
    /// DocumentGroup(newDocument: BookDocument()) { file in
    ///     ContentView(document: file.$document)
    /// }
    ///
    /// // Несколько независимых окон:
    /// WindowGroup("Editor") { ContentView() }
    /// WindowGroup("Settings") { SettingsView() }
    ///
    /// // Только для macOS: панель настроек
    /// #if os(macOS)
    /// Settings { SettingsView() }
    /// #endif
    /// ```
    var body: some Scene {
        // ─────────────────────────────────────────────────────
        // ГРУППА ОКОН С ЕДИНЫМ КОРНЕВЫМ ПРЕДСТАВЛЕНИЕМ
        // WindowGroup создаёт "шаблон" окна, который может быть инстанцирован многократно
        WindowGroup {
            // Корневой View приложения — точка входа в иерархию интерфейса
            // Здесь инициализируется ContentView со своим @State и логикой
            ContentView()
            
            // 💡 Совет: для инъекции зависимостей используйте .environmentObject:
            // ContentView()
            //     .environmentObject(viewModel) // Доступно во всех вложенных View
        }
        // ─────────────────────────────────────────────────────
        // СТИЛЬ ОКНА: АВТОМАТИЧЕСКОЕ ОПРЕДЕЛЕНИЕ ПЛАТФОРМОЙ
        // .windowStyle(.automatic) — значение по умолчанию, можно опустить
        //
        // Доступные опции (в основном для macOS):
        // • .automatic — платформа выбирает стиль (обычно .titleBar для macOS)
        // • .titleBar — классическое окно с заголовком и кнопками управления
        // • .hiddenTitleBar — заголовок скрыт, контент занимает всю область (как в Photos)
        // • .plain — минималистичное окно без украшений
        //
        // На iOS этот модификатор игнорируется (там только один стиль окна)
        .windowStyle(.automatic)
        
        // ====================================================================
        // MARK: - 💡 ВОЗМОЖНОСТИ РАСШИРЕНИЯ КОНФИГУРАЦИИ
        // ====================================================================
        
        /*
         // ─────────────────────────────────────────────────────
         // 1. НАСТРОЙКА РАЗМЕРОВ ОКНА (macOS)
         // Для десктопного приложения полезно задать начальный размер:
         #if os(macOS)
         .windowDefaults {
             $0.defaultSize = CGSize(width: 1200, height: 800)
             $0.minSize = CGSize(width: 800, height: 600)
         }
         #endif
         
         // ─────────────────────────────────────────────────────
         // 2. ДОБАВЛЕНИЕ ПАНЕЛИ НАСТРОЕК (macOS)
         // Позволяет пользователю настроить приложение через системный интерфейс:
         #if os(macOS)
         Settings {
             SettingsView() // Ваш View с настройками
                 .environmentObject(viewModel)
         }
         #endif
         
         // ─────────────────────────────────────────────────────
         // 3. ОБРАБОТКА ЖИЗНЕННОГО ЦИКЛА ПРИЛОЖЕНИЯ
         // Для реакции на запуск/закрытие/переход в фон используйте:
         @Environment(\.scenePhase) private var scenePhase
         
         var body: some Scene {
             WindowGroup { ContentView() }
                 .onChange(of: scenePhase) { newPhase in
                     switch newPhase {
                     case .active:
                         print("✅ Приложение активно — можно обновлять UI")
                         viewModel.refreshData()
                     case .inactive:
                         print("⏸ Приложение неактивно — пауза анимаций")
                     case .background:
                         print("💤 Приложение в фоне — сохраним данные")
                         viewModel.autosave()
                     @unknown default:
                         break
                     }
                 }
         }
         
         // ─────────────────────────────────────────────────────
         // 4. ПОДДЕРЖКА ОТКРЫТИЯ ФАЙЛОВ ЧЕРЕЗ СИСТЕМУ (macOS/iOS)
         // Для обработки drag&drop файлов или открытия через "Открыть с помощью":
         .handlesExternalEvents(preferring: ["txt", "json"], allowing: ["*"])
         .onOpenURL { url in
             // Обработка URL файла, переданного системе
             viewModel.openFile(url: url)
         }
         
         // ─────────────────────────────────────────────────────
         // 5. КОНФИГУРАЦИЯ ДЛЯ РАЗНЫХ ПЛАТФОРМ
         // Используйте условную компиляцию для платформенно-специфичного кода:
         #if os(macOS)
         .commands {
             // Добавление пунктов в меню приложения
             CommandGroup(after: .appInfo) {
                 Button("Проверить обновления") {
                     // Логика проверки обновлений
                 }
             }
         }
         #elseif os(iOS)
         // На iOS можно настроить ориентацию, статус-бар и т.д.
         #endif
         
         // ─────────────────────────────────────────────────────
         // 6. ГЛОБАЛЬНАЯ ИНЪЕКЦИЯ ЗАВИСИМОСТЕЙ
         // Чтобы ViewModel была доступна во всём приложении без прокидывания:
         var sharedViewModel: ContentViewModel = .init()
         
         var body: some Scene {
             WindowGroup {
                 ContentView()
                     .environmentObject(sharedViewModel) // Доступно через @EnvironmentObject
             }
         }
         
         // Тогда в любом вложенном View:
         // @EnvironmentObject var viewModel: ContentViewModel
         */
    }
    
    // ========================================================================
    // MARK: - 🔄 ИНИЦИАЛИЗАЦИЯ И ЖИЗНЕННЫЙ ЦИКЛ
    // ========================================================================
    
    /*
     // ─────────────────────────────────────────────────────
     // ИНИЦИАЛИЗАЦИЯ ПРИЛОЖЕНИЯ
     // Код в init() выполняется один раз при запуске приложения
     // Подходит для:
     // • Настройки глобальных сервисов (логирование, аналитика)
     // • Проверки лицензий, первой настройки
     // • Предзагрузки кэшей, конфигураций
     
     init() {
         // Пример: настройка глобального стиля для всего приложения
         UITableView.appearance().backgroundColor = .clear
         
         // Пример: инициализация системы логирования
         Logger.shared.setup(level: .debug)
         
         // Пример: проверка и загрузка автосохранения
         // (лучше делать это в ViewModel, но можно и здесь)
         let viewModel = ContentViewModel()
         viewModel.loadAutosaveIfNeeded()
     }
     
     // ─────────────────────────────────────────────────────
     // ВАЖНО: Избегайте тяжёлых операций в init() и body!
     // • init(): выполняется до отрисовки, но блокирует запуск если слишком долгий
     // • body(): может вызываться многократно при перерисовках — только декларативное описание!
     
     // Правильный паттерн для "тяжёлого" старта:
     @StateObject private var appController = AppController()
     
     var body: some Scene {
         WindowGroup {
             if appController.isReady {
                 ContentView()
             } else {
                 LoadingView() // Показываем индикатор загрузки
                     .onAppear { appController.startup() }
             }
         }
     }
     */
}

