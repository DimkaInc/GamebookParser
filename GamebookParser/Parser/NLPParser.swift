/// Парсер для извлечения именованных сущностей из текста с помощью машинного обучения.
/// - Использует встроенный в Apple NLTagger (Natural Language Tagging)
/// - Не требует сетевого соединения: все модели работают локально на устройстве
/// - Поддерживает множественные языки автоматически (определяется по тексту)
///
/// ## Что такое NER (Named Entity Recognition)?
/// Это задача автоматического извлечения из текста упоминаний объектов реального мира:
/// - 👤 Персоны: "Гэндальф", "Наполеон", "Мария Склодовская-Кюри"
/// - 🌍 Локации: "Средиземье", "Париж", "река Волга"
/// - 🏢 Организации: "Хогвартс", "ООО Ромашка", "ООН"
/// - 📅 Даты/времени: "12 декабря 2024", "в полдень"
///
/// ## Преимущества NLTagger:
/// ✅ **Конфиденциальность**: обработка происходит на устройстве, текст не уходит в сеть
/// ✅ **Производительность**: оптимизированные нативные модели, быстрый запуск
/// ✅ **Мультиязычность**: автоматически определяет язык и применяет соответствующую модель
/// ✅ **Простота**: минимум кода для мощного функционала
///
/// ## Ограничения:
/// ⚠️ **Точность**: локальные модели менее точны, чем облачные LLM (GPT, Claude)
/// ⚠️ **Набор сущностей**: фиксированный список типов (нельзя добавить "магический предмет")
/// ⚠️ **Контекст**: не понимает глубокий контекст, только поверхностные паттерны
/// ⚠️ **Размер моделей**: первая загрузка может занять время (модели кэшируются системой)
///
/// ## Пример использования:
/// ```swift
/// let parser = NLPParser()
/// let text = "Гэндальф встретил Фродо в Шире около 3000 года Третьей Эпохи."
/// let names = parser.extractEntities(from: text)
/// // names: ["Гэндальф", "Фродо"] (персоны)
/// // Для локаций нужен другой tagScheme: .placeName
/// ```
final class NLPParser {
    
    // MARK: - 👤 ИЗВЛЕЧЕНИЕ ПЕРСОНАЛЬНЫХ ИМЁН
    /// Находит и возвращает все упоминания имён собственных (персон) в тексте.
    /// - Parameter text: Исходный текст для анализа (любой длины, любой кодировки)
    /// - Returns: Массив строк с найденными именами в порядке появления в тексте
    /// - Сложность: O(n), где n = количество токенов (слов) в тексте
    ///
    /// ## Как это работает (под капотом):
    /// 1. **Токенизация**: текст разбивается на слова/токены с учётом языка
    /// 2. **POS-тегирование**: каждому токену присваивается часть речи (сущ., глагол, ...)
    /// 3. **NER-классификация**: модель определяет, является ли последовательность токенов именем
    /// 4. **Объединение**: опция .joinNames склеивает многословные имена ("Лев Николаевич")
    ///
    /// ## Поддерживаемые языки (автоматическое определение):
    /// 🇷🇺 Русский, 🇬🇧 Английский, 🇩🇪 Немецкий, 🇫🇷 Французский, 🇪🇸 Испанский,
    /// 🇮🇹 Итальянский, 🇵🇹 Португальский, 🇨🇳 Китайский, 🇯🇵 Японский, 🇰🇷 Корейский
    /// + ещё ~15 языков. Полный список: NLTagger.supportedLanguages
    ///
    /// ## Примеры работы:
    /// ```
    /// Вход: "Арагорн и Леголас пошли в Минас Тирит."
    /// Выход: ["Арагорн", "Леголас"] (Минас Тирит — локация, не извлекается этим методом)
    ///
    /// Вход: "Dr. John Smith from NASA visited Paris."
    /// Выход: ["John Smith"] (.joinNames объединил имя и фамилию)
    ///
    /// Вход: "" (пустая строка)
    /// Выход: [] (пустой массив, без ошибок)
    /// ```
    func extractEntities(from text: String) -> [String] {
        // ─────────────────────────────────────────────────────
        // ШАГ 1: ИНИЦИАЛИЗАЦИЯ ТАГГЕРА
        // NLTagger — основной класс для лингвистической аннотации текста
        // Принимает массив схем тегирования, которые будем использовать
        let tagger = NLTagger(tagSchemes: [.nameType])
        // .nameType — схема для извлечения имён собственных:
        // • .personalName (имена людей)
        // • .placeName (географические названия)
        // • .organizationName (компании, учреждения)
        
        // ─────────────────────────────────────────────────────
        // ШАГ 2: УСТАНОВКА ТЕКСТА ДЛЯ АНАЛИЗА
        // Присваиваем текст таггеру — это триггерит внутреннюю предобработку:
        // • Определение языка (по содержимому, не по локали устройства)
        // • Загрузка соответствующей языковой модели (если ещё не в кэше)
        // • Токенизация и построение лингвистических признаков
        tagger.string = text
        
        // Результирующий массив для накопления найденных имён
        var entities: [String] = []
        
        // ─────────────────────────────────────────────────────
        // ШАГ 3: ОПРЕДЕЛЕНИЕ ДИАПАЗОНА ОБРАБОТКИ
        // Работаем со всем текстом: от первого до последнего символа
        // Используем String.Index (не Int), чтобы корректно обрабатывать:
        // • Юникод-символы (эмодзи, иероглифы, составные буквы)
        // • Графемные кластеры (например: "é" = e + combining accent)
        let range = text.startIndex..<text.endIndex
        
        // ─────────────────────────────────────────────────────
        // ШАГ 4: ПЕРЕБОР ТЕГОВ С ОБРАБОТКОЙ КАЖДОГО ТОКЕНА
        // enumerateTags — итеративный метод, вызывающий замыкание для каждого найденного тега
        tagger.enumerateTags(
            in: range,           // Диапазон текста для анализа
            unit: .word,         // Единица токенизации: .word, .sentence, .paragraph, .token
            scheme: .nameType,   // Схема тегов: какие типы сущностей искать
            options: [.joinNames] // Флаги обработки (см. ниже)
        ) { tag, tokenRange in
            // ─────────────────────────────────────────────────────
            // ЗАМЫКАНИЕ-ОБРАБОТЧИК: вызывается для каждого потенциального имени
            // Параметры:
            // • tag: NLTag? — найденный тип сущности (.personalName, .placeName, ...)
            //          nil, если токен не распознан как именованная сущность
            // • tokenRange: Range<String.Index> — диапазон символов в исходном тексте
            
            // Фильтруем: оставляем только имена персонажей
            // Другие типы (.placeName, .organizationName) игнорируются в этом методе
            // Для извлечения других типов — добавьте дополнительные условия:
            // if tag == .personalName || tag == .placeName { ... }
            if tag == .personalName {
                // Извлекаем подстроку по найденному диапазону
                // String(text[tokenRange]) — безопасный способ слайсинга строк в Swift
                entities.append(String(text[tokenRange]))
            }
            
            // Возвращаемое значение замыкания:
            // • true  — продолжить перебор следующих токенов (стандартное поведение)
            // • false — досрочно прервать перебор (оптимизация: нашли что искали)
            // Пример раннего выхода: если нужно только первое имя в тексте
            //   if tag == .personalName { entities.append(...); return false }
            return true
        }
        
        // ─────────────────────────────────────────────────────
        // ШАГ 5: ВОЗВРАТ РЕЗУЛЬТАТА
        // Возвращаем массив имён в порядке их появления в тексте
        // Дубликаты не удаляются автоматически: "Фродо встретил Фродо" → ["Фродо", "Фродо"]
        // Для удаления дубликатов: return Array(Set(entities)) — но потеряется порядок
        return entities
    }
    
    // ========================================================================
    // MARK: - 💡 РАСШИРЕНИЕ ФУНКЦИОНАЛА (ИДЕИ ДЛЯ БУДУЩЕГО)
    // ========================================================================
    
