/// Протокол, определяющий контракт для плагинов обработки страниц.
/// - Реализует паттерн "Plugin Architecture" / "Extension Points"
/// - Позволяет добавлять новую логику парсинга без изменения ядра системы
/// - Каждый плагин — независимый модуль с уникальным именем и логикой обработки
///
/// ## Преимущества такого подхода:
/// ✅ **Открытость/Закрытость (SOLID)**: новые функции добавляются новыми типами, а не правкой существующих
/// ✅ **Изоляция**: каждый плагин тестируется независимо от остальных
/// ✅ **Гибкость**: плагины можно включать/выключать через конфигурацию
/// ✅ **Порядок контроля**: менеджер плагинов управляет последовательностью выполнения
///
/// ## Пример реализации плагина:
/// ```swift
/// struct KeywordExtractor: ParserPlugin {
///     var name: String { "keyword_extractor" }
///
///     func process(page: inout Page) {
///         // Логика: найти все слова в кавычках и добавить в теги страницы
///         let text = page.text.joined()
///         let keywords = extractQuotedWords(from: text)
///         page.tags.append(contentsOf: keywords)
///     }
/// }
/// ```
protocol ParserPlugin {
    
    /// Уникальное идентификатор плагина для логирования, отладки и конфигурации.
    /// - Рекомендуется использовать snake_case или kebab-case для единообразия
    /// - Примеры: "gold_detector", "ai-summarizer", "link-validator"
    /// - Используется для:
    ///   • Логирования: "✓ Plugin 'gold_detector' applied"
    ///   • Конфигурации: отключение конкретного плагина в настройках
    ///   • Метрик: замер времени выполнения каждого плагина отдельно
    var name: String { get }
    
    /// Основной метод обработки, вызываемый для каждой страницы.
    /// - Parameter page: Ссылка на страницу для модификации "на месте" (inout)
    /// - Важные особенности:
    ///   • Метод может изменять любые свойства страницы: text, items, actions, stats
    ///   • Изменения применяются напрямую к оригинальному объекту (экономия памяти)
    ///   • Порядок вызова плагинов контролируется PluginManager
    ///   • Ошибки внутри плагина не обрабатываются автоматически — добавляйте try/catch при необходимости
    ///
    /// ## Рекомендации по реализации:
    /// 1. **Идемпотентность**: повторный вызов с теми же данными должен давать тот же результат
    /// 2. **Изоляция**: плагин не должен полагаться на побочные эффекты других плагинов (если не документировано)
    /// 3. **Производительность**: избегайте тяжёлых операций в основном потоке; для AI/сети используйте async
    /// 4. **Безопасность**: валидируйте входные данные, не доверяйте сырому тексту
    func process(page: inout Page)
}

