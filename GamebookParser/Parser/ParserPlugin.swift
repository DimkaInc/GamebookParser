/// Центральный реестр и диспетчер для управления плагинами парсинга.
/// - Реализует паттерн "Plugin Registry" + "Chain of Responsibility"
/// - Отвечает за регистрацию, хранение и последовательный запуск плагинов
/// - Позволяет динамически настраивать конвейер обработки под разные задачи
///
/// ## Типичный сценарий использования:
/// ```swift
/// // 1. Создаём менеджер
/// let pluginManager = PluginManager()
///
/// // 2. Регистрируем нужные плагины (порядок важен!)
/// pluginManager.register(GoldDetectionRule())
/// pluginManager.register(ExperienceExtractor())
/// pluginManager.register(AIEnrichmentPlugin())
///
/// // 3. Обрабатываем страницу через весь конвейер
/// var page = parseRawPage(from: text)
/// pluginManager.process(page: &page)
/// // → page теперь содержит данные от всех плагинов
/// ```
///
/// ## Расширение через конфигурацию (пример для продакшена):
/// ```swift
/// struct PluginConfig {
///     var enabled: [String] = ["gold", "experience"] // по имени плагина
///     var order: [String] = ["gold", "experience"]   // явный порядок выполнения
/// }
///
/// // Тогда регистрация может быть динамической:
/// for pluginName in config.order where config.enabled.contains(pluginName) {
///     if let plugin = PluginFactory.create(pluginName) {
///         manager.register(plugin)
///     }
/// }
/// ```
final class PluginManager {
    
    // MARK: - 📦 ХРАНИЛИЩЕ ПЛАГИНОВ
    /// Внутренний массив зарегистрированных плагинов.
    /// - Плагины хранятся как [ParserPlugin] — полиморфный массив через протокол
    /// - Порядок элементов определяет последовательность выполнения (первый → последний)
    /// - Приватный доступ: модификация только через метод register() для контроля целостности
    ///
    /// ## Почему массив, а не словарь?
    /// ✅ Порядок выполнения важен и контролируется явно
    /// ✅ Допускаются несколько экземпляров одного типа плагина с разными настройками
    /// ✅ Простота итерации: for plugin in plugins { ... }
    ///
    /// ## Если нужна быстрая проверка по имени:
    /// Можно добавить вспомогательный словарь для ускорения lookup:
    /// private var pluginsByName: [String: ParserPlugin] = [:]
    private var plugins: [ParserPlugin] = []
    
    // MARK: - 🔌 РЕГИСТРАЦИЯ ПЛАГИНА
    /// Добавляет плагин в конец очереди обработки.
    /// - Parameter plugin: Экземпляр, соответствующий протоколу ParserPlugin
    /// - Важные замечания:
    ///   • Плагин добавляется в конец: последним зарегистрированным = последним выполненным
    ///   • Дубликаты не проверяются автоматически: можно зарегистрировать один плагин дважды (намеренно или по ошибке)
    ///   • Потокобезопасность: метод не атомарный; при многопоточной регистрации нужна синхронизация
    ///
    /// ## Пример регистрации с логированием:
    /// ```swift
    /// func register(_ plugin: ParserPlugin) {
    ///     plugins.append(plugin)
    ///     logger.debug("✓ Registered plugin: \(plugin.name)")
    /// }
    /// ```
    func register(_ plugin: ParserPlugin) {
        // Простое добавление в конец массива
        // Порядок регистрации = порядок выполнения в process()
        plugins.append(plugin)
    }
    
    // MARK: - ⚙️ ЗАПУСК ОБРАБОТКИ ЧЕРЕЗ ВСЕ ПЛАГИНЫ
    /// Последовательно применяет все зарегистрированные плагины к переданной странице.
    /// - Parameter page: Ссылка на страницу для модификации "на месте" (inout)
    /// - Сложность: O(P × C), где P = количество плагинов, C = средняя сложность одного плагина
    ///
    /// ## Поток выполнения:
    /// ```
    /// Вход: page (сырые данные)
    /// ↓
    /// plugin[0].process() → page изменён
    /// ↓
    /// plugin[1].process() → видит изменения от plugin[0]
    /// ↓
    /// ...
    /// ↓
    /// plugin[N].process() → финальные изменения
    /// ↓
    /// Выход: page (обогащённый данными от всех плагинов)
    /// ```
    ///
    /// ## Важные особенности:
    /// 1. **Каскадные изменения**: каждый плагин работает с результатом предыдущих
    ///    - Это позволяет строить зависимые цепочки (например, сначала извлечь золото, потом применить налог)
    ///    - Но требует документирования зависимостей между плагинами!
    ///
    /// 2. **Отсутствие короткого замыкания**: все плагины выполняются всегда
    ///    - Если один плагин "упал" с ошибкой — остальные продолжат работу (если не добавлен try/catch)
    ///    - Для продакшена рекомендуется добавить обработку ошибок и логирование
    ///
    /// 3. **inout-семантика**: страница изменяется напрямую, без возврата нового объекта
    ///    - Экономит память (нет копирования большой модели)
    ///    - Требует явного &page при вызове (сигнал о мутации для разработчика)
    func process(page: inout Page) {
        // ─────────────────────────────────────────────────────
        // ПОСЛЕДОВАТЕЛЬНЫЙ ОБХОД ВСЕХ ЗАРЕГИСТРИРОВАННЫХ ПЛАГИНОВ
        for plugin in plugins {
            // Вызываем основной метод обработки плагина
            // &page — явная передача по ссылке, требуемая протоколом
            plugin.process(page: &page)
            
            // 💡 Совет для отладки и мониторинга:
            // Добавьте логирование для трассировки выполнения:
            // print("✓ Plugin '\(plugin.name)' processed page \(page.id)")
            
            // 💡 Совет для метрик:
            // Замеряйте время выполнения каждого плагина:
            // let start = Date()
            // plugin.process(page: &page)
            // let duration = Date().timeIntervalSince(start)
            // metrics.record(plugin: plugin.name, time: duration)
        }
        
        // После цикла страница содержит совокупные изменения от всех плагинов
        // Возврат не требуется: модификации применены напрямую
    }
}

