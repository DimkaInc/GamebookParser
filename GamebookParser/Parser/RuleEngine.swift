/// Универсальный движок для применения набора правил парсинга к странице.
/// - Реализует паттерн "Chain of Responsibility" / "Strategy"
/// - Каждое правило инкапсулирует одну логику извлечения данных (золото, опыт, предметы)
/// - Правила применяются последовательно, модифицируя страницу "на месте"
/// - Архитектура позволяет легко добавлять новые правила без изменения существующего кода
///   (принцип Open/Closed из SOLID)
///
/// ## Пример потока данных:
/// ```
/// 1. Сырая страница из парсера:
///    Page(id: 5, text: ["Вы нашли 50 золотых!"], items: [:])
///
/// 2. RuleEngine.process(page: &page):
///    → GoldDetectionRule.apply(): находит "50 золотых" → добавляет в items
///    → (будущий) ExperienceRule.apply(): находит "+10 опыта" → добавляет в stats
///
/// 3. Обработанная страница:
///    Page(id: 5, text: [...], items: ["gold": 50], stats: ["xp": 10])
/// ```
///
/// ## Почему это удобно:
/// ✅ Новые правила добавляются одной строкой в массив `rules`
/// ✅ Каждое правило тестируется изолированно
/// ✅ Порядок применения правил контролируется явно
/// ✅ Легко отключать/включать правила через конфигурацию
final class RuleEngine {
    
    // MARK: - 📋 РЕЕСТР ПРАВИЛ
    /// Массив активных правил парсинга, применяемых к каждой странице.
    /// - Порядок важен: правила выполняются последовательно сверху вниз
    /// - Каждое правило получает уже модифицированную предыдущими правилами страницу
    /// - Тип [ParsingRule] — протокол, что позволяет хранить разнородные правила в одном массиве
    ///
    /// ## Как добавить новое правило:
    /// 1. Создать структуру/класс, соответствующий протоколу ParsingRule
    /// 2. Реализовать метод apply(to page: inout Page)
    /// 3. Добавить экземпляр в этот массив:
    ///    ```
    ///    private let rules: [ParsingRule] = [
    ///        GoldDetectionRule(),
    ///        ExperienceRule(),      // ← новое правило
    ///        ItemDropRule()         // ← ещё одно
    ///    ]
    ///    ```
    private let rules: [ParsingRule] = [
        GoldDetectionRule() // Единственное активное правило на данный момент
        // Пример: ищет паттерн "(\d+)\s+золот" и добавляет золото в инвентарь
    ]
    
    // MARK: - ⚙️ ПРИМЕНЕНИЕ ПРАВИЛ К СТРАНИЦЕ
    /// Последовательно применяет все зарегистрированные правила к переданной странице.
    /// - Parameter page: Ссылка на страницу для модификации "на месте" (inout-параметр)
    /// - Возвращаемое значение: Void (страница изменяется напрямую)
    /// - Сложность: O(R × P), где R = количество правил, P = сложность одного правила
    ///
    /// ## Важные особенности:
    /// 1. inout-параметр: страница передаётся по ссылке, а не по значению
    ///    - Это экономит память (не копируется вся модель)
    ///    - Позволяет правилам модифицировать оригинальный объект
    ///    - Требует явного указания &page при вызове (сигнал о мутации)
    ///
    /// 2. Последовательность выполнения:
    ///    - Правило 2 видит изменения, внесённые правилом 1
    ///    - Это может быть как преимуществом (зависимые правила), так и риском (порядок важен!)
    ///
    /// 3. Отсутствие короткого замыкания:
    ///    - Все правила выполняются всегда, даже если одно из них "упало"
    ///    - Для продакшена стоит добавить обработку ошибок (try/catch внутри rule.apply)
    func process(page: inout Page) {
        // ─────────────────────────────────────────────────────
        // ПОСЛЕДОВАТЕЛЬНЫЙ ОБХОД ВСЕХ ЗАРЕГИСТРИРОВАННЫХ ПРАВИЛ
        for rule in rules {
            // Применяем текущее правило к странице
            // &page — явная передача по ссылке (inout), требуемая протоколом ParsingRule
            rule.apply(to: &page)
            
            // 💡 Совет для отладки:
            // Можно добавить логирование для трассировки применения правил:
            // print("✓ Applied \(type(of: rule)) to page \(page.id)")
        }
        
        // После выхода из цикла страница содержит все изменения от всех правил
        // Возврат не требуется: модификации применены напрямую к оригинальному объекту
    }
}
