import Foundation

// ============================================================================
// MARK: - ОЧИСТИТЕЛЬ ТЕКСТА: НОРМАЛИЗАЦИЯ СЫРОГО КОНТЕНТА
// ============================================================================

/// Утилита для предобработки и нормализации текста перед парсингом.
/// - Реализован как enum с static-методами (неинстанцируемый помощник)
/// - Основная задача: преобразовать "разорванный" текст (по строкам файла) в логические абзацы
/// - Использует эвристики на основе пунктуации и регистра для принятия решений об объединении
///
/// ## Проблема, которую решает этот класс:
/// Исходные текстовые файлы часто имеют разрывы строк не по смыслу, а по техническим причинам:
/// - Ограничение ширины строки в старом редакторе (например, 80 символов)
/// - Экспорт из PDF/Word с автоматическими переносами
/// - Ручное форматирование автором "для читаемости в блокноте"
///
/// ## Решение:
/// Алгоритм последовательно читает строки и решает: объединить текущую с предыдущей
/// или начать новый абзац. Критерии:
/// 1. Если предыдущая строка НЕ заканчивается на [.!?;:] — вероятно, предложение не закончено
/// 2. Если следующая строка начинается с маленькой буквы — это продолжение мысли
/// 3. Пустые строки всегда разрывают абзацы
///
/// ## Пример преобразования:
/// ```
/// ВХОД (сырой файл):
/// Вы вошли в тёмную комнату.
/// В углу мерцает свеча,
/// а на столе лежит ключ.
///
/// Что будете делать?
///
/// ВЫХОД (нормализованный массив абзацев):
/// [
///   "Вы вошли в тёмную комнату.",
///   "В углу мерцает свеча, а на столе лежит ключ.",
///   "Что будете делать?"
/// ]
/// ```
enum TextCleaner {
    
    // MARK: - 🔄 ОСНОВНОЙ МЕТОД НОРМАЛИЗАЦИИ
    /// Преобразует сырой многострочный текст в массив логических абзацев.
    /// - Parameter text: Исходный текст с произвольными разрывами строк
    /// - Returns: Массив строк, где каждая — законченный смысловой абзац
    /// - Сложность: O(n), где n = количество символов в тексте
    static func normalize(_ text: String) -> [String] {
        // ─────────────────────────────────────────────────────
        // ШАГ 1: РАЗБИЕНИЕ НА СЫРЫЕ СТРОКИ
        // components(separatedBy: .newlines) разбивает текст по символам переноса:
        // - \n (Unix/Linux/macOS)
        // - \r\n (Windows)
        // - \r (старые Mac)
        let lines = text.components(separatedBy: .newlines)
        
        // Результирующий массив для накопления готовых абзацев
        var result: [String] = []
        
        // Буфер для накопления текущего формируемого абзаца
        // Постепенно "растёт" по мере объединения строк
        var current = ""
        
        // ─────────────────────────────────────────────────────
        // ШАГ 2: ПОСТРОЧНЫЙ ОБХОД С ПРИНЯТИЕМ РЕШЕНИЙ
        for rawLine in lines {
            // Очистка строки от ведущих/замыкающих пробелов и невидимых символов
            // Это важно: "  текст  " и "текст" должны считаться одинаково
            let line = rawLine.trimmingCharacters(in: .whitespacesAndNewlines)
            
            // Пропускаем полностью пустые строки (они уже выполнили свою роль — разделили абзацы)
            guard !line.isEmpty else {
                continue
            }
            
            // ─────────────────────────────────────────────────────
            // ЛОГИКА НАКОПЛЕНИЯ АБЗАЦА
            
            // Если буфер пуст — это начало нового абзаца
            if current.isEmpty {
                current = line
                continue // Переходим к следующей строке без дополнительных проверок
            }
            
            // Если буфер не пуст — решаем: объединить с текущей строкой или нет?
            if shouldMerge(current: current, next: line) {
                // Объединяем через пробел: сохраняем читаемость и разделяем слова
                current += " " + line
            } else {
                // Не объединяем: текущий абзац завершён
                // 1. Сохраняем его в результат
                result.append(current)
                // 2. Начинаем новый абзац с текущей строки
                current = line
            }
        }
        
        // ─────────────────────────────────────────────────────
        // ШАГ 3: ФИНАЛИЗАЦИЯ
        // После цикла в буфере может остаться последний абзац — не забываем его добавить!
        if !current.isEmpty {
            result.append(current)
        }
        
        // Возвращаем массив нормализованных абзацев
        return result
    }
    
    // MARK: - 🧠 ЭВРИСТИКА ОБЪЕДИНЕНИЯ СТРОК
    /// Определяет, следует ли объединить две соседние строки в один абзац.
    /// - Parameters:
    ///   - current: Уже накопленная часть абзаца (контекст "слева")
    ///   - next: Следующая строка из файла (контекст "справа")
    /// - Returns: true, если строки логически принадлежат одному абзацу
    ///
    /// ## Правила принятия решения:
    /// 1. Если `current` заканчивается на [.!?;:] — это конец предложения/мысли → НЕ объединять
    /// 2. Если `next` начинается с заглавной буквы — вероятно, новое предложение → НЕ объединять
    /// 3. Если `next` начинается с маленькой буквы — продолжение предыдущей мысли → объединить
    /// 4. Пустые строки обрабатываются на уровне normalize() и сюда не попадают
    ///
    /// ## Примеры:
    /// ```
    /// current: "Он открыл дверь," + next: "и вошёл в комнату" → true (запятая + маленькая буква)
    /// current: "Он открыл дверь." + next: "В комнате было темно" → false (точка + заглавная)
    /// current: "Свет погас!" + next: "что делать?" → true (восклицание, но маленькая буква)
    /// ```
    private static func shouldMerge(
        current: String,
        next: String
    ) -> Bool {
        // ─────────────────────────────────────────────────────
        // ПРОВЕРКА 1: АНАЛИЗ ПОСЛЕДНЕГО СИМВОЛА ТЕКУЩЕЙ СТРОКИ
        guard let last = current.last else {
            // Пустая строка не должна попадать сюда (защита от ошибок вызова)
            return false
        }
        
        // Список символов, которые обычно завершают смысловой блок:
        // . ! ? — конец предложения
        // : ; — пауза, после которой часто следует новая мысль или список
        let endings: [Character] = [".", "!", "?", ":", ";"]
        
        // Если строка заканчивается на один из этих символов — НЕ объединяем
        // Это эвристика: автор, скорее всего, намеренно поставил знак завершения
        if endings.contains(last) {
            return false
        }
        
        // ─────────────────────────────────────────────────────
        // ПРОВЕРКА 2: АНАЛИЗ ПЕРВОГО СИМВОЛА СЛЕДУЮЩЕЙ СТРОКИ
        guard let first = next.first else {
            // Пустая строка — защита от ошибок
            return false
        }
        
        // Ключевая эвристика:
        // Если следующая строка начинается с маленькой буквы —
        // это с высокой вероятностью продолжение предыдущего предложения,
        // разорванное техническим переносом строки
        return first.isLowercase
    }
}
